let array = ["maçã", "pera", "seila"];

//o push coloca itens no final do array e o pop tira o valor do final do array
array.push("opa");
console.log(array)
array.pop();
console.log(array)
//o shift e unshift fazem a mesma coisa mas no começo do array
array.unshift("opa");
console.log(array)
array.shift();
console.log(array)
